    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (lblupRoomType.getText().equalsIgnoreCase("single")){
             Single ho = new Single(lblstastus1.getText(),Double.parseDouble(lblroomsize.getText()) ,txtDescription.getText(),lblstastus.getText(),txtgender.getText(),Double.parseDouble(lblbathroomsize.getText()),Integer.parseInt(lblbathroomuser.getText()));
             ho.setMonthlyRent(txtupPrice.getText());
             ho.setDeposit(Double.parseDouble(txtupPrice.getText()));
             ho.bupdateHostelinformation(fileHandling.HostelFile, roomID);
             
        }
        else if(lblupRoomType.getText().equalsIgnoreCase("doubleroom")){
            DoubleRoom ho1 = new DoubleRoom(lblstastus1.getText(),Double.parseDouble(lblroomsize.getText()) ,txtDescription.getText(),lblstastus.getText(),txtgender.getText(),Double.parseDouble(lblbathroomsize1.getText()),lblbathroomuser1.getText());
            ho1.setMonthlyRent(txtupPrice.getText());
   
            ho1.setNumberOfResidents(lblbathroomuser1.getText());
            ho1.bupdateHostelinformation(fileHandling.HostelFile, roomID);
        }
        else if(lblupRoomType.getText().equalsIgnoreCase("master")){
            Master ho2= new Master(lblstastus1.getText(),Double.parseDouble(lblroomsize.getText()) ,txtDescription.getText(),lblstastus.getText(),txtgender.getText(),Double.parseDouble(lblbathroomsize2.getText()));
            ho2.setMonthlyRent(txtupPrice.getText());
            ho2.bupdateHostelinformation(fileHandling.HostelFile, roomID);
        }
        //applicationid, username,name,email,roomid,type,monthlyrent,deposit,duration,year,month,status
        else {
            Unit ho3= new Unit(lblstastus1.getText(),txtgender.getText(),Double.parseDouble(lblroomsize.getText()) ,lblstastus.getText(),txtDescription.getText(),Integer.parseInt(lblroomnumber.getText()),Integer.parseInt(lblbathroomnumber.getText()));
            ho3.setMonthlyRent(txtupPrice.getText());
            ho3.bupdateHostelinformation(fileHandling.HostelFile, roomID);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

