    public UpdateHostelInformation(String id,String type) {
       
        initComponents();
         this.roomID = id;
         this.type = type;
         
         if(!id.equalsIgnoreCase("")){
            Utility ult = new Utility();
            ult.setId(id);
            ult.setType(type);
            if(type.equalsIgnoreCase("single")){
                
                jPanel3.removeAll();
                jPanel3.add(singleroomspecification);
                jPanel3.repaint();
                jPanel3.revalidate();
                ult.setFileName(fileHandling.HostelFile);
                ult.displayOnPanel(jPanel1);
            }else if (type.equalsIgnoreCase("doubleroom")){
                jPanel3.removeAll();
                jPanel3.add(doubleroomspecification);
                jPanel3.repaint();
                jPanel3.revalidate();
                ult.setFileName(fileHandling.HostelFile);
                ult.displayOnPanel(jPanel1);
            }else if (type.equalsIgnoreCase("master")){
                jPanel3.removeAll();
                jPanel3.add(masterroomspecification);
                jPanel3.repaint();
                jPanel3.revalidate();
                
                ult.setFileName(fileHandling.HostelFile);
                ult.displayOnPanel(jPanel1);
            }else {
                jPanel3.removeAll();
                jPanel3.add(unitspecification);
                jPanel3.repaint();
                jPanel3.revalidate();
                ult.setFileName(fileHandling.HostelFile);
                ult.displayOnPanel(jPanel1);
            }
           
         }
    }
